export function READING_GUIDE_PROMPT(
  title: string,
  bookSummary: string,
  summariesText: string
): string {
  return `
你是一位经验丰富的阅读教练。

请基于下面提供的全书摘要与章节摘要，为这本书生成一份「阅读指南」。

目标：
帮助读者在正式阅读前，快速理解这本书为什么值得读，并带着问题进入阅读。

输出必须严格使用以下结构：

## 背景化
说明：
- 作者试图解决什么问题
- 这本书大致处于什么问题背景、知识背景或现实背景
- 适合什么读者

## 阅读三问
提出 3 个关键问题，让读者带着问题去读。
要求：
- 三个问题不能重复
- 三个问题必须覆盖整本书核心逻辑
- 问题要具体，不要空泛

## 当代意义
说明：
- 这本书对今天的读者有什么启发
- 它解决哪些现实问题
- 哪类人最值得读

其他要求：
1. 使用中文
2. 总字数控制在 300-500 字
3. 像知识笔记，不像聊天回答
4. 不要逐章复述
5. 不要夸张吹捧
6. 不要输出除上述结构外的额外标题

书名：
${title}

全书摘要：
${bookSummary}

章节摘要：
${summariesText}
`;
}
