export function VIEW_MAP_PROMPT(
  title: string,
  bookSummary: string,
  summariesText: string
): string {
  return `
你是一位逻辑分析师。

请基于下面提供的全书摘要与章节摘要，为这本书生成一份「观点地图」。

目标：
把一本书从线性文本转换为结构化观点，帮助读者快速理解整本书的核心逻辑。

输出必须严格使用以下结构：

## 中心观点
用 1 段话提炼整本书最核心的判断。
要求：
- 不能只是主题名词
- 必须是一个清晰判断

## 主要观点
列出 3-5 条支撑中心观点的主要观点。
要求：
- 每条观点都应是完整判断
- 不要写成章节标题

## 关键论据
按"主要观点"分别列出对应的关键支撑内容，可包括：
- 概念
- 论据
- 案例
- 方法
要求：
- 每条主要观点下给出 2-4 条关键支撑
- 以简洁列表呈现

## 信息噪声
指出书中可能存在的冗余或低增量内容，例如：
- 重复表达
- 修辞性强调
- 对中心观点贡献较小的内容

## 说服力评估
评价整本书的论证质量，包括：
- 逻辑是否完整
- 支撑是否充分
- 是否存在明显短板

其他要求：
1. 使用中文
2. 总字数控制在 350-600 字
3. 像知识笔记，不像聊天回答
4. 不要逐章复述
5. 不要输出除上述结构外的额外标题

书名：
${title}

全书摘要：
${bookSummary}

章节摘要：
${summariesText}
`;
}
