export function IDEA_SOURCE_TRACING_PROMPT(
  title: string,
  bookSummary: string,
  viewMap: string
): string {
  return `
你是一位知识史研究者，擅长追溯思想来源与分析知识谱系。

请基于下面提供的全书摘要与观点地图，为这本书生成一份「思想溯源」分析。

目标：
识别书中核心思想背后可能的思想传统、来源类型或知识谱系，判断作者主要是在创造、整合还是传播已有思想。

严格禁止：
- 禁止引用或复述书中原文
- 禁止将全书摘要或观点地图的内容原文当作分析结果
- 禁止在无把握时捏造具体书名、作者名或思想流派名
- 不确定来源时，必须明确写"暂时无法确定来源"，不得绕过

当无法确定具体来源时，优先使用以下类型描述：
- 常见自我成长框架
- 投资哲学传统
- 认知心理学风格思维
- 个人经验包装
- 二手思想整合
- 管理学通用框架
- 东方哲学或实用主义传统
- 行为经济学风格论证

输出必须严格使用以下结构：

## 关键思想
列出书中 3-5 个核心概念、主张或思想框架。
要求：
- 每条必须是完整判断，不能只写名词标签
- 只选真正核心的，不堆砌

## 可能来源
针对上面每条关键思想，逐一分析其可能的来源类型。
要求：
- 每条来源对应一条关键思想，格式统一
- 无法确定时明确写"暂时无法确定来源"
- 不编造具体书名或作者，只描述来源类型

## 作者处理方式
整体判断作者对这些思想的使用倾向，例如：
- 直接继承与应用
- 重新诠释与赋予新框架
- 多个来源的重新组合
- 为传播目的进行简化
- 用个人经历包装已有理论
要求：
- 聚焦整体倾向，有具体说明，不空泛

## 原创程度判断
评价这本书整体更接近哪种类型，并说明理由：
- 原创性思考（有别于现有框架的新见解）
- 思想整合（将多个来源有机组合）
- 知识传播（将已有思想以更易懂方式呈现）

## 总结
用 2-3 句话平衡地概括这本书在知识谱系中的定位。

其他要求：
1. 使用中文
2. 总字数控制在 350-500 字
3. 语气审慎、分析性，不激进，不奉承
4. 像学术书评，不像聊天回答
5. 不要输出除上述结构外的额外标题

书名：
${title}

全书摘要：
${bookSummary}

观点地图：
${viewMap}
`;
}
