export function IDEA_SOURCE_TRACING_PROMPT(
  title: string,
  bookSummary: string,
  summariesText: string
): string {
  return `
你是一位知识史研究者，擅长追溯思想来源与分析知识谱系。

请基于下面提供的全书摘要与章节摘要，为这本书生成一份「思想溯源」分析。

目标：
帮助读者理解书中的核心思想可能源自哪里，作者是在创造新知识、整合已有理论，还是在传播普及既有思想。

重要原则：
- 不要凭空捏造具体的书名或作者名
- 如果无法确认某个思想的来源，请明确说明"无法从现有内容判断来源"
- 保持审慎、客观，不过度断言

输出必须严格使用以下结构：

## 关键思想
列出书中 3-5 个重要的概念、主张或思想框架。
要求：
- 每条用一句话概括，清晰具体
- 只列真正核心的思想，不堆砌

## 可能来源
针对上面每条关键思想，分析其可能的来源类型，例如：
- 已有的思想流派或学科传统
- 经典理论或哲学框架
- 常见自我成长或管理领域的通用框架
- 作者自身经历与实践总结
- 无法从现有内容判断来源
要求：
- 每条来源分析对应一条关键思想
- 不确定时直接说不确定，不编造来源

## 作者处理方式
判断作者对这些思想的使用方式，例如：
- 直接继承与应用
- 重新诠释与赋予新框架
- 多个来源的重新组合
- 为传播目的进行简化
- 用个人经历包装已有理论
要求：
- 聚焦整体倾向，不逐条罗列
- 有具体说明，不空泛

## 原创程度判断
评价这本书整体上更接近哪种类型：
- 原创性思考（作者提出了有别于现有框架的新见解）
- 思想整合（将多个来源的思想有机组合）
- 知识传播（将已有思想用更易懂的方式呈现给大众）
并给出理由。

## 总结
用 2-3 句话，平衡地概括这本书在知识谱系中的定位。

其他要求：
1. 使用中文
2. 总字数控制在 400-600 字
3. 语气审慎、分析性，不激进，不奉承
4. 像学术书评，不像聊天回答
5. 不要输出除上述结构外的额外标题

书名：
${title}

全书摘要：
${bookSummary}

章节摘要：
${summariesText}
`;
}
