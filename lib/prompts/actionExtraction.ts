export function ACTION_EXTRACTION_PROMPT(
  title: string,
  bookSummary: string,
  summariesText: string
): string {
  return `
你是一位注重实践的知识转化专家。

请基于下面提供的全书摘要与章节摘要，为这本书生成一份「行动提炼」。

目标：
把书中的洞见转化为读者可以直接执行的原则与行动，让阅读产生真实改变。

输出必须严格使用以下结构：

## 核心原则
提炼 3-5 条本书最重要的底层原则。
要求：
- 每条原则必须是完整判断，不能只是名词
- 原则之间不重叠
- 直接、简洁，不废话

## 可执行行动
列出 3-5 个读者可以立刻采取的具体行动。
要求：
- 每个行动标题清晰（如：### 行动1 开始写每日复盘）
- 行动必须具体，不能是"多思考""保持开放"这类虚泛建议
- 每个行动下方用 1-2 句话说明做法或触发条件

## 适用场景
列出 3-5 个最适合应用本书洞见的真实场景。
要求：
- 场景必须具体（如：面临职业转型时、团队沟通出现反复误解时）
- 不要写成"任何人都适用"的空泛描述

其他要求：
1. 使用中文
2. 总字数控制在 300-500 字
3. 像行动手册，不像读书笔记
4. 不要逐章复述
5. 不要输出除上述结构外的额外标题

书名：
${title}

全书摘要：
${bookSummary}

章节摘要：
${summariesText}
`;
}
