export function CRITICAL_EXAMINATION_PROMPT(
  title: string,
  bookSummary: string,
  summariesText: string
): string {
  return `
你是一位严谨的批判性思维分析师。

请基于下面提供的全书摘要与章节摘要，对这本书进行批判性分析，生成一份「观点校验」。

目标：
不是否定这本书，而是帮助读者用更清醒的视角理解它——知道它在哪里说得好、在哪里可能有漏洞。

输出必须严格使用以下结构：

## 核心观点复述
用中立的语言简短复述作者的核心主张（不加评价，只是还原）。
要求：
- 不超过 100 字
- 不带情绪，不吹捧，不批评

## 可能存在的问题
列出 2-4 个本书论证中可能存在的逻辑漏洞、前提假设或过度简化之处。
要求：
- 每条问题必须具体，指向书中的某个论点
- 不要泛泛说"缺乏证据"，要说明哪个结论缺乏支撑

## 反例或不同视角
提供 2-3 个与本书观点相对立或补充的视角，可来自：
- 其他思想家的理论
- 现实案例
- 本书内部的矛盾
要求：
- 每个反例或视角要具体，有出处或场景
- 不要只是"也有人持相反意见"这种空泛说法

## 适用边界
说明本书的观点在什么条件下有效、在什么条件下可能失效。
要求：
- 给出 2-3 个具体的适用场景
- 给出 1-2 个明显不适用的场景

其他要求：
1. 使用中文
2. 总字数控制在 350-550 字
3. 语气分析性、平衡，不激进，不偏袒
4. 像学术书评，不像聊天回答
5. 不要输出除上述结构外的额外标题

书名：
${title}

全书摘要：
${bookSummary}

章节摘要：
${summariesText}
`;
}
